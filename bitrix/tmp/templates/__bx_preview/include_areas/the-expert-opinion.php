<p>
  Экспертное заключение может выдаваться Заказчику в одном или более экземплярах.
</p>
<p>
  В экспертном заключении описываются общие характеристики объекта, фотография общего вида 
  объекта, фотографии дефектов, описывается, почему это является дефектом, даётся ссылка на 
  нормативную документацию (выдержки из СНиПов, ГОСТов, ТУ, инструкций производителей, технологических карт),  
  протоколы испытаний, ответы на поставленные перед экспертной организацией вопросы, выводы и 
  рекомендации. Также прикладываются копии дипломов и сертификатов экспертов, свидетельства СРО, 
  которые и дают официальное право на проведение <a href="/stroitelnaya-ekspertiza/">строительных экспертиз</a> 
  зданий и сооружений.
</p>
<p>
  Экспертное заключение состоит из:
</p>
<ol>
  <li><span class="number fL">1</span>Титульный лист</li>
  <li><span class="number fL">2</span>Оглавление</li>
  <li><span class="number fL">3</span>Основание для производства экспертного заключения</li>
  <li><span class="number fL">4</span>
