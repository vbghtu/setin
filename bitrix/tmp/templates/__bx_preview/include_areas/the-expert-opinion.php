    образование, специальность, стаж работы)
  </li>
  <li><span class="number fL">5</span>Подписка о предупреждении экспертов об ответственности за дачу заведомо ложного заключения</li>
  <li><span class="number fL">6</span>Вопросы, поставленные перед экспертной организацией</li>
  <li><span class="number fL">7</span>Общие данные объекта исследования</li>
  <li><span class="number fL">8</span>Представленная документация</li>
