<p>
  <span style="font-style:Italic;">Что собой представляет <strong>Устная консультация?</strong></span>
</p>
<ol>
  <li><span class="number fL">1</span>Выезжает эксперт на объект обследования</li>
  <li><span class="number fL">2</span>Проводит анализ существующей документации по объекту</li>
  <li><span class="number fL">3</span>Изучает его объемно-планировочные и конструктивные параметры</li>
  <li><span class="number fL">4</span>В устной форме объясняются Заказчику основные моменты по объекту (дефекты, рекомендации)</li>
  <li><span class="number fL">5</span>Ответы на вопросы по объекту</li>
  <li><span class="number fL">6</span>Составление Акта осмотра (на месте)</li>
  <li><span class="number fL">7</span>Составление Дефектной ведомости (на месте)</li>
</ol>
<p>
  Данная услуга важна тем Заказчикам, которым необходимо для самих себя понять, что собой представляет объект, тем, которые не будут обращаться в суд. 
  И чаще всего именно данную услугу заказывают перед покупкой недвижимости, чтобы выяснить техническое состояние строения, чтобы определиться - приобретать или нет,  
  так как полноценная экспертиза с выдачей отчёта обойдется несравнимо дороже. Также стоит отметить, что данная строительная экспертиза отличается оперативностью. 
  В течение одной встречи на объекте с экспертом Заказчик в режиме живого общения получает основную информацию о строении, между тем стандартная экспертиза по времени 
  подготовки документа для Заказчика по времени обойдётся до двух календарных недель от даты выезда эксперта на обследование строения.
</p>
<p>
  При устной консультации для разрешения проблемы Эксперт использует тот же комплект оборудования и инструментария, в основу которого входят средства измерения 
  геометрических параметров строения – нивелир, уклономер, рулетка и иные приборы, исходя из поставленных задач Заказчиком. Кроме того, используются неразрушающие 
  средства контроля состояния конструкций, приборы для определения плотности бетона и наличия арматуры в теле железобетонных конструкций, влагомер и т. д.
</p>
<p>
  Некоторым Заказчикам наших услуг важно и обследование участка перед началом строительства, выбор местоположения дома на участке, ориентация отдельных комнат, 
  то есть Заказчику важно помочь составить техническое задание на проектирование дома на уже приобретённом участке, а это без выезда Эксперта не сделать. 
  Необходимо оценить уклоны местности, в зависимости от местных условий и существующих строений на соседствующих участках предстоит дать рекомендации по устройству 
  будущих коммуникаций, а это, в свою очередь, неизбежно повлияет на планировку помещений будущего дома.
</p>
<p>
  В дальнейшем возможно предложение услуг <a href="/tehnadzor/">технического надзора</a> за будущим строительством для Заказчика, такие услуги позволят держать весь ход проводимых работ 
  под серьёзным контролем, что в конечном итоге позволит Заказчику сэкономить деньги и личное время.
</p>
