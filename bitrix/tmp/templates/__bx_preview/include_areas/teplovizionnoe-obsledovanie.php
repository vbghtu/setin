<p><b>Что такое тепловизионное обследование?</b></p>
 
<p>Тепловизионное обследование – это проверка теплотехнических параметров ограждающих конструкций зданий и сооружений с помощью специального прибора – тепловизора. В данном приборе применим метод термограмм. На экран дисплея выводятся зоны одинаковых температур излучения исследуемой поверхности, которые можно зафиксировать, и по которым определяются теплопотери.</p>
 
<p><b>Для чего нужно тепловизионное обследование?</b></p>
 
<p>Тепловизионное обследование позволяет выявить зоны потерь тепла дистанционно, следовательно, позволяет оперативно, точечно, с наименьшими затратами провести необходимые теплоизоляционные работы. Проверка зданий держать тепло поможет определить фактические объёмы при капитальном ремонте, при покупке объекта недвижимости, при регламентных работах различной сложности. Без данного вида обследования невозможно оформить энергетический паспорт здания.</p>
 
