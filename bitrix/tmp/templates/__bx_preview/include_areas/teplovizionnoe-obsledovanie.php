<p><b>Как выполняется тепловизионное обследование?</b></p>
 
<p>Тепловизионное обследование предусматривает термографическую съёмку интересующих участков ограждающих конструкций – стен, кровли, окон и т. д. Для корректного проведения обследования необходимо раздвинуть шторы на окнах, удалить посторонние предметы с подоконников, из углов, примыкающих к наружным стенам, обеспечить доступ к плинтусам наружных стен. Сделать это необходимо за 3 часа до начала проведения работ.</p>
 
<p><b>Как оформляется результат тепловизионного обследования?</b></p>
 
<p>Результаты тепловизионного обследования оформляются в табличной форме на бумажном носителе, при этом распечатываются изображения термограмм, полученные в ходе обследования с помощью тепловизора с нанесёнными и зафиксированными точками конкретного определения температур на проекциях исследуемых поверхностей. В исследуемых точках фиксируется температура поверхности. Распечатка производится параллельно с фото исследуемой поверхности. Сами термограммы в зависимости от температуры поверхности различаются по цветам, тут же выносится столбик соответствия температуры вполне определённому цвету.</p>
 
