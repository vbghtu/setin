          <h1>Ландшафтное проектирование и дизайн в Москве</h1>
		  
<div class="row"><div class="col-md-12">		  
		  
<div class="newh2">Элементы ландшафтного дизайна</div><br />

<p>Прежде всего, это строения. Исходя из технической возможности и экономической целесообразности, а также принятых правил застройки участка в составе коттеджного посёлка, противопожарных разрывов и проч, композиционной основой, центром, становится самое большое строение.</p>
<p><img style="float:left;margin: 7px 7px 7px 0;width:250px;height:250px;border: 1px solid #c0d0e0;padding: 3px;" src="/upload/medialibrary/c85/image001.jpg" />В зависимости от существующих уклонов местности, располагаются функциональные объекты жизнеобеспечения, в том числе и подземные, такие, как контурный дренаж вокруг коттеджа, автономные очистные сооружения, иные подземные коммуникации.</p><p>В дальнейшем схема раскладки всех систем повлияет на размещение других объектов ландшафта.</p>
<p>Основная цель ландшафтного дизайна – грамотно «вписать» существующее строение в проектируемый ландшафт, сбалансировать, сгладить тяжесть давления объёмов, углов, прямых линий строения на окружающую природу.</p><p>Если же задачу поставить с определённой целью, предполагая какое-то своё видение всего пространства ещё до строительства коттеджа, то тогда и разработка эскиза самого дома будет зависеть от того или иного стиля, заданного изначально заказчиком строения.</p>

