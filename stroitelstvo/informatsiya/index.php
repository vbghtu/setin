<div class="newh2">1. Проблемы малоэтажного строительства</div> <br />

<p>Любое строительство должно базироваться на ряде основных положений, которые до сих пор не воспринимаются всерьёз некоторыми небольшими и средними строительными компаниями, выбравшими для себя эту нишу на строительном рынке.<img src="/bitrix/templates/techno/images/inf1.jpg" style="float: right;margin: 7px 0 7px 7px;width:350px;border: 1px solid #c0d0e0;padding: 3px;"></p>

<p>Следует отметить, что здесь условия работы строительных компаний существенно отличаются от общепринятого формата строительства, и прежде всего некоторой сезонностью проведения части работ. Исходя из экономической целесообразности, многим мелким и средним компаниям невыгодно держать постоянный штат своих сотрудников весь год – строительных рабочих, линейных инженерно-технических работников, архитекторов, конструкторов. Такие компании не в состоянии обеспечивать круглогодичное строительство по заданным параметрам, не в состоянии нести дополнительные расходы, обеспечивающие соблюдение технологий строительства в зимнее время.</p>

<p>Зимний период показан для строительства, но необходимо следовать нормативным документам, закладывать расходы по обеспечению необходимых технологических условий для ведения работ, гибко использовать современные технологии, содержать необходимую технологическую базу. Не для всех компаний это выгодно. Кроме того, узость специализации многих строительных фирм негативно сказывается на сезонных заказах, пути приложения интересов сокращаются, и как результат – падение качества строительства, нарушение технологических цепочек строительного процесса.</p>

<p>Постоянная ротация кадров не способствует качественному строительству чего-либо, будь это многоквартирный дом или небольшой коттедж. Качество строительно-монтажных работ – категория многих составляющих, начиная от изысканий на участке застройки и заканчивая соблюдением технологии отделочных работ на завершающей стадии перед сдачей объекта Заказчику.</p>

