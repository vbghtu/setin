<div class="newh2">5. Приёмка объекта</div> <br />

<p>Непосредственно перед приёмкой выверяются объёмы выполненных работ, проверяется качество работ, собирается вся документация по объекту для передачи Заказчику. Повторно проверяются все коммуникации «на эффект». Этому служат следующие мероприятия:</p>

<ul style="padding-left:15px;">
<li>Подготовка папки исполнительной документации для передачи Заказчику, актов приёмки объекта;</li>
<li>Подписание Акта приёмки объекта в соответствии с Договором строительного подряда;</li>
<li>Передача комплекта чертежей проекта, сметы, исполнительной документации, гарантийного сертификата Заказчику.</li>
