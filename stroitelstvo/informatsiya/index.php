<div class="newh2">3. Система качества строительства</div><br />

<p>Прежде всего, стоит отметить, что основная цель работы нашей компании – это не просто построить коттедж, а построить <b><i>качественно коттедж</i></b>, и потому у нас разработана и введена трёхступенчатая система контроля качества строительства. Так случается иногда, что в конечном итоге все ошибки, недочёты, а иногда и мелкие хитрости некоторых участников строительства приходится исправлять в ходе самого строительства, и тут практически вся ответственность ложится на плечи бригадира строителей, в лучшем случае, производителя работ. Теперь представьте, что лично Вам именно в Вашей работе придётся исправлять чужие ошибки…</p>

<p>Но наша принципиальная позиция – каждый должен отвечать за то дело, которое ему поручено, бригадир имеет право строить только из качественного материала, и в его обязанностях наряду с прорабом – не допустить ошибок при строительстве.</p>

