<li>Соблюдение технологических процессов строительно-монтажных работ в соответствии с проектно-сметной документацией, технологическими картами;</li>
<li>Своевременное проведение мероприятий, обеспечивающих технику безопасности в строительстве в соответствии с СНиП;</li>
<li>Постоянный контроль хода строительства, как объёмов выполненных работ, так и качества, включая инструментальный контроль;</li>
<li>оформление необходимой исполнительной документации: актов на скрытые работы, исполнительных схем, ведение журналов работ, журнала входного контроля, актов геодезической съёмки и проч.;</li>
<li>Промежуточная приёмка этапов работ, в том числе и пуско-наладочных, отслеживание объёмов выполненных работ в соответствии с формами КС-2; КС-3;</li>
<li>Согласование и утверждение возникающих изменений по инициативе Заказчика в ходе строительства в проектно-сметной документации;</li>
<li>Постоянный контроль графика строительства, внесение необходимых коррективов в строительный процесс с целью ведения работ в плановом порядке.</li>
</ul>

