<p>Получение разрешительных документов требует предварительного сбора предусмотренного законодательством пакета документации и определенных трат. Согласование сложной перепланировки квартиры или нежилого помещения обойдется собственнику несколько дороже, чем выполнение работ по стандартному проекту.</p>

<p>Базовый пакет, который нужно собрать, чтобы получить согласование перепланировки по эскизу, включает:</p>

<ul>
<li>паспорт (если собственник – гражданин), уставные документы (для владельцев-юридических лиц);</li>
<li>свидетельство о праве собственности на недвижимое имущество, иные правоустанавливающие документы;</li>
<li>технический паспорт на квартиру/дом, поэтажный план, экспликация (подготовкой, а также выдачей этой документации занимается БТИ);</li>
<li>письменное согласие собственников, членов семьи владельца, проживающих в данном помещении;</li>
<li>проектная документация относительно запланированных изменений.</li>
