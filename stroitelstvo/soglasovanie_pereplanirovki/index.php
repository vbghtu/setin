          <h1>Согласование перепланировки помещений</h1>
		  
<div class="row"><div class="col-md-12">		  
		  
<p>Решение о выполнении перепланировки по индивидуальному проекту принимается в соответствии с потребностями, пожеланиями, нуждами конкретного собственника. Но произвольно выполнить такие изменения нельзя. Модифицировать помещение по индивидуальному проекту самопроизвольно нельзя. Необходимо согласование проекта перепланировки квартиры/дома/офиса компетентными государственными и/или местными органами. </p>

<div class="newh2">Нормативно-правовая база</div><br />

<p>Требование относительно того, что согласование перепланировки должно быть получено в предусмотренных законом случаях, установлено рядом нормативно-правовых актов. Прежде всего, это Жилищный Кодекс Российской Федерации от 29.12.2004 г., а также региональное законодательство (инструкции, постановления), которые подробно регламентируют все вопросы, касающиеся процесса согласования (перечень оснований, наименование уполномоченных госорганов, сроки и цены, согласование перепланировки нежилых помещений).</p>

<p>Перед проведением любых строительно-ремонтных мероприятий рекомендуется ознакомиться с соответствующей законодательной базой или проконсультироваться у специалистов. Так вы будете точно знать, требуется ли согласование перепланировки в БТИ, других инстанциях.</p>

<div class="newh2">Когда необходимо получение разрешительных документов</div><br />

<p>Перепланировка нежилого помещения может осуществлять по одному из двух сценариев:</p>

<ul>
<li>для отдельно стоящего объекта нежилого предназначения — согласование в БТИ обязательно;</li>
<li>на первом этаже жилого здания — необходимо согласование в жилищной инспекции, а также с жильцами.</li>
</ul>

<p>Узаконить перепланировку необходимо, если в нежилом помещении:</p>

<ul>
<li>возводятся стены/перегородки, за счет чего увеличивается нагрузка на несущие конструкции;</li>
<li>переоборудуются/меняется расположение санитарных зон;</li>
<li>меняются размеры/форма оконных и дверных проемов;</li>
<li>устанавливается оборудование, требующее дополнительных коммуникаций;</li>
<li>модифицируется пол;</li>
<li>перестраиваются/ликвидируются лестничные проемы и так далее.</li>
</ul>

<p>Процесс включает такие стадии:</p>

<p>1. Получение документов в БТИ.</p>

<p>2. Разработка проекта перепланировки — эту работу выполняют специализированные компании, имеющие необходимое разрешение.</p>

<p>3. Согласование проекта в госинстанциях.</p>

<p>4. Направление документации, прошедшей согласование, в жилищную горинспекцию.</p>

<p>5. Получение разрешения на старт работ по перепланировке.</p>

<p>6. Выполнение перепланировки нежилого помещения, прием работ и подписание акта об их завершении.</p>

<p>7. Предоставление акта в БТИ, после чего изменения вносятся в кадастровый паспорт.</p>

<p>8. Получение новых документов.</p>

<p>9. Отражение произведенных модификаций в госсвидетельстве о праве собственности.</p>

<div class="newh2">Санкции при отсутствии согласования</div><br />

<p>Соблазн отказаться от выполнения бюрократических формальностей велик, однако, важно помнить, что последствия от несоблюдения требований законодательства могут оказаться затратными для собственника недвижимости:</p>

<ul>
<li>Не полученное вовремя согласование перепланировки нежилого помещения в жилом доме чревато штрафом, суммы которого варьируются для физических или юридических лиц.</li>
<li>Самовольная перепланировка может стать основанием для вынесения инспектором особого предписания, в котором будет указано: «необходимо до истечения установленного срока получить согласование перепланировки жилых помещений либо за собственный счет ликвидировать все внесенные изменения». Это придется выполнить.</li>
<li>До тех пор, пока законным образом не будет получено согласование перепланировки помещений, объект нельзя продать, подарить, обменять, другим образом выполнить отчуждение.</li>
<li>Могут возникнуть проблемы с оформлением наследства на такое недвижимое имущество.</li>
</ul>

